# Cortado de cadenas
# cadena[inicio:fin:direccion]

# ejercicio 1
#              1         2         3
#    01234567890123456789012345678901234
msg="SI SUPIERAS LO TANTO QUE TE QUIERO"
# ejercicio 1
print(msg[0:1]) # Mostrara  s
# ejercicio 2
print(msg[0:2]) # Mostrara  si
# ejercicio 3
print(msg[0:2]+msg[3]) # Mostrara  sis
# ejercicio 4
print(msg[5:8]) # Mostrara  pie
# ejercicio 5
print(msg[15:])  # Mostrara tanto que te quiero
# ejercicio 7
print(msg[:])   # Mostrara toda al cadena
# ejercicio 8
print(msg[::-1]) # Invertir la cadena
